     * @return array<int, Column>
     */
    public function columns(): array
    {
        return [
            Column::make('ID', 'id')
                ->searchable()
                ->hidden(),

            Column::make('Created at', 'created_at')
                ->hidden(),

            Column::make('Tanggal / Waktu', 'created_at_formatted', 'created_at')
                ->searchable()
                ->sortable(),

            Column::make('Tipe Laporan', 'type')
                ->searchable()
                ->sortable(),

            Column::make('Pesan', 'messages')
                ->searchable(),

            Column::make('Jumlah User Tersampaikan', 'user_delivered')
                ->sortable()

        ];
    }

    /**
     * PowerGrid Filters.
